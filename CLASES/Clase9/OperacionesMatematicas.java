package Clase9;

public class OperacionesMatematicas {
    //Atributos
    private int a;
    private int b;

    //Constructor: Permite inicializar la clase
    public OperacionesMatematicas(int a,int b){
        this.a = a;
        this.b = b;
    }

    //Metodos
    public int sumar(){
        return a + b;
    }
}
